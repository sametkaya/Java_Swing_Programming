    private void btn_ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ekleActionPerformed
        // TODO add your handling code here:
        Hasta yenihasta = new Hasta();
        yenihasta.Id = txt_hastaId.getText();
        yenihasta.Adi = txt_hastaName.getText();
        yenihasta.Soyadi = txt_hastaSoyad.getText();
        yenihasta.Doktor = cmbx_hastaDoktor.getSelectedItem().toString();

        Hasta.Hastalar.add(yenihasta);

        dtm.setRowCount(0);
        for (Hasta hasta : Hasta.Hastalar) {
            dtm.addRow(new Object[]{hasta.Id, hasta.Adi, hasta.Soyadi, hasta.Doktor});
        }
        Listele();
        Reset();

    }//GEN-LAST:event_btn_ekleActionPerformed

    private void btn_silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_silActionPerformed
        // TODO add your handling code here:
        if (tbl_liste.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(rootPane, "Bir Hasta Seçmelisiniz");
            return;
        }
        Hasta.Hastalar.remove(tbl_liste.getSelectedRow());

        Listele();
        Reset();


    }//GEN-LAST:event_btn_silActionPerformed

    private void btn_sorgulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sorgulaActionPerformed
        // TODO add your handling code here:
        dtm.setRowCount(0);
        for (Hasta hasta : Hasta.Hastalar) {
            if (hasta.Id.startsWith(txt_hastaId.getText())) {
                dtm.addRow(new Object[]{hasta.Id, hasta.Adi, hasta.Soyadi, hasta.Doktor});
            }

        }


    }//GEN-LAST:event_btn_sorgulaActionPerformed
     Hasta degisecekHasta;
    private void btn_degistirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_degistirActionPerformed
        // TODO add your handling code here:
         if (tbl_liste.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(rootPane, "Bir Hasta Seçmelisiniz");
            return;
        }
        degisecekHasta.Id = txt_hastaId.getText();
        degisecekHasta.Adi = txt_hastaName.getText();
        degisecekHasta.Soyadi = txt_hastaSoyad.getText();
        degisecekHasta.Doktor = cmbx_hastaDoktor.getSelectedItem().toString();
        Listele();
 
        
    }//GEN-LAST:event_btn_degistirActionPerformed

