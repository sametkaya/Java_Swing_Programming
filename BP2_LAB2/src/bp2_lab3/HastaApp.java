    private void tbl_listeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_listeMouseClicked
        // TODO add your handling code here:
           if (tbl_liste.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(rootPane, "Bir Hasta Seçmelisiniz");
            return;
        }
        degisecekHasta= Hasta.Hastalar.get(tbl_liste.getSelectedRow());
        txt_hastaId.setText(degisecekHasta.Id);
        txt_hastaName.setText(degisecekHasta.Adi);
        txt_hastaSoyad.setText(degisecekHasta.Soyadi);
        cmbx_hastaDoktor.setSelectedItem(degisecekHasta.Doktor); 
    }//GEN-LAST:event_tbl_listeMouseClicked

    private void Reset() {
        txt_hastaId.setText("");
        txt_hastaName.setText("");
        txt_hastaSoyad.setText("");
        cmbx_hastaDoktor.setSelectedIndex(0);
    }

    private void Listele() {
        dtm.setRowCount(0);
        for (Hasta hasta : Hasta.Hastalar) {
            dtm.addRow(new Object[]{hasta.Id, hasta.Adi, hasta.Soyadi, hasta.Doktor});
        }
    }

